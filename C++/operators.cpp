#include <iostream>
using namespace std;

int main() {
    // +,-,*,%,/
    cout << 5+2 << endl;
    cout << 5-2 << endl;
    cout << 5*2 << endl;
    cout << 5/2 << endl;
    cout << 5%2 << endl;

    // Unary operators ++, --
    int counter = 7;
    counter ++;
    cout << counter << endl;
    cout << --counter << endl;
}